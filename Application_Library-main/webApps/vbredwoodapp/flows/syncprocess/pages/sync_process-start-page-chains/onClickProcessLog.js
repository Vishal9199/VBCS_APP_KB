define([
  'vb/action/actionChain',
  'vb/action/actions',
  'vb/action/actionUtils',
], (
  ActionChain,
  Actions,
  ActionUtils
) => {
  'use strict';

  class onClickProcessLog extends ActionChain {

    /**
     * @param {Object} context
     * @param {Object} params
     * @param {object} params.event
     * @param {any} params.originalEvent
     */
    async run(context, { event, originalEvent }) {
      const { $page, $flow, $application, $constants, $variables } = context;

      const toSchedulesync = await Actions.navigateToFlow(context, {
        flow: 'schedulesync',
        target: 'parent',
        page: 'schedule_sync-start',
      });
    }
  }

  return onClickProcessLog;
});
